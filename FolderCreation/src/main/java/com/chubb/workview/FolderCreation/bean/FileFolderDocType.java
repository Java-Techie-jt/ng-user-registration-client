package com.chubb.workview.FolderCreation.bean;

public class FileFolderDocType {

	private String FileTypeDesc;
	private String FolderTypeDesc;
	private String DocTypeDesc;
	private int FileTypeCode;
	private int FolderTypeCode;
	private int DocTypeCode;

	public String getFileTypeDesc() {
		return FileTypeDesc;
	}

	public void setFileTypeDesc(String fileTypeDesc) {
		FileTypeDesc = fileTypeDesc;
	}

	public String getFolderTypeDesc() {
		return FolderTypeDesc;
	}

	public void setFolderTypeDesc(String folderTypeDesc) {
		FolderTypeDesc = folderTypeDesc;
	}

	public String getDocTypeDesc() {
		return DocTypeDesc;
	}

	public void setDocTypeDesc(String docTypeDesc) {
		DocTypeDesc = docTypeDesc;
	}

	public int getFileTypeCode() {
		return FileTypeCode;
	}

	public void setFileTypeCode(int fileTypeCode) {
		FileTypeCode = fileTypeCode;
	}

	public int getFolderTypeCode() {
		return FolderTypeCode;
	}

	public void setFolderTypeCode(int folderTypeCode) {
		FolderTypeCode = folderTypeCode;
	}

	public int getDocTypeCode() {
		return DocTypeCode;
	}

	public void setDocTypeCode(int docTypeCode) {
		DocTypeCode = docTypeCode;
	}

}
